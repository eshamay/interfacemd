#ifndef UTILITY_H_
#define UTILITY_H_

using namespace std;

// These RUN macros should clean up code for checking through vectors and arrays
#ifndef RUN

#define RUN(list)   \
   	for (int i = 0; i < (int)list.size(); i++)

#endif

#ifndef RUN2

#define RUN2(list)  \
   	for (int j = 0; j < (int)list.size(); j++)

#endif

#endif
